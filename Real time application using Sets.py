A = {"tamil","english","urdu","telugu","malayalam"};
B = {"french","japaneese","tamil","malayalam","hindi"};
# union
print("Union :", A | B)
# intersection
print("Intersection :", A & B)
# difference
print("Difference :", A - B)
# symmetric difference
print("Symmetric difference :", A ^ B)
